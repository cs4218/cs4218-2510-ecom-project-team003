// filters

export const buildProductFiltersArgs = (checked, radio) => {
  let args = {};
  if (checked.length > 0) {
    args.category = checked;
  }
  if (radio.length === 2) {
    args.price = { $gte: radio[0], $lte: radio[1] };
  }
  return args;
};

export const productFiltersController = async (req, res) => {
  try {
    const result = productFiltersSchema.safeParse(req.body);
    if (!result.success) {
      return res.status(400).send({
        success: false,
        message: 'Invalid request',
        error: z.prettifyError(result.error),
      });
    }
    const { checked, radio } = result.data;
    const args = buildProductFiltersArgs(checked, radio);
    const products = await productModel.find(args);
    res.status(200).send({
      success: true,
      message: "Filtered products fetched",
      products,
    });
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      message: "Error while filtering products",
      error: error.message,
    });
  }
};

